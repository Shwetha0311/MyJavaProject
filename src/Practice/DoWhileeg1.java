package Practice;

public class DoWhileeg1 {

	public static void main(String[] args) {
		// Code to print multiples of a number using the do while loop
		int i=1; int num=10;
		System.out.println("Multiples of the number");
		do {
			System.out.println("10 x " +i +" = " +num*i);
			i=i+1;
		}while(i<=10);

	}

}
