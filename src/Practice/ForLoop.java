package Practice;

public class ForLoop {

	public static void main(String[] args) {
		/* COde to print the multiples of a number
		 * using for loop
		 */
		int table =9;
		for (int i=1;i<=10;i++)
		{
			System.out.println(table +"X"  + i+ "="  + table*i);
			
			
			
		}

	}

}
